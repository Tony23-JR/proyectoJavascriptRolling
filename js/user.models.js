class User {
  constructor(id, name, lastName, type, age, country, city, passport, address, email) {
    this.id = id;
    this.name = name;
    this.lastName = lastName;
    this.type = type;
    this.age = age;
    this.country = country;
    this.city = city;
    this.passport = passport;
    this.address = address;
    this.email = email;
  }
}
